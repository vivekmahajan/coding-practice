class PairingPawns {
public:
	int savedPawnCount(vector <int>);
};

int PairingPawns::savedPawnCount(vector <int> start) {
    if(start.size()==1)
        return start[0];
    for(int i=start.size()-1; i>=1; i--){
        int prawns = start[i];
        start[i-1] += prawns/2;

    }
    return start[0];
}


