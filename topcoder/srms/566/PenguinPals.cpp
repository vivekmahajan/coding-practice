class PenguinPals {
public:
	int findMaximumMatching(string);
};

int PenguinPals::findMaximumMatching(string colors){
    int result = 0;
    stack< char > st;
    for(int i=0; i<colors.size(); i++){
        char cur = colors[i];
        if(st.size()==0){
            st.push(cur);
            continue;
        }
        if(st.size()==1){
            if(st.top()!=cur){
                st.push(cur);
            }else{
                st.pop();
                result ++;
            }
            continue;
        }else{
            while(!st.empty()){
                char t = st.top();
                st.pop();
                if(t == cur){
                    result ++;
                    continue;
                }
            }

        }

    }
    return result;
}


